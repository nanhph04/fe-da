const VIETNAMESE_BACKEND_ERROR_MESSAGES = new Map<string, string>(
  VIETNAMESE_BACKEND_ERROR_MESSAGE_ENTRIES,
);

export function translateBackendErrorMessage(message: string, locale: string): string {
  if (locale !== "vi") {
    return message;
  }

  const normalizedMessage = message.trim();
  if (!normalizedMessage) {
    return message;
  }

  const exactTranslation = VIETNAMESE_BACKEND_ERROR_MESSAGES.get(normalizedMessage);
  if (exactTranslation) {
    return exactTranslation;
  }

  for (const dynamicTranslation of DYNAMIC_VIETNAMESE_BACKEND_ERROR_TRANSLATIONS) {
    const match = normalizedMessage.match(dynamicTranslation.pattern);
    if (match) {
      return dynamicTranslation.translate(match);
    }
  }

  return message;
}
